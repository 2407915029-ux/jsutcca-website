export type TranslationKey = string;

export function translate(language: Language, key: TranslationKey, values?: Record<string, string | number>) {
  const text = readPath(translations[language], key) ?? readPath(translations.zh, key) ?? key;
  if (typeof text !== "string") return key;
  return interpolate(text, values);
}

function readPath(tree: TranslationTree, key: string): string | TranslationTree | undefined {
  return key.split(".").reduce<string | TranslationTree | undefined>((current, part) => {
    if (!current || typeof current === "string") return undefined;
    return current[part];
  }, tree);
}

function interpolate(text: string, values?: Record<string, string | number>) {
  if (!values) return text;
  return text.replace(/\{\{(\w+)\}\}/g, (_, name: string) => String(values[name] ?? ""));
}
